export const worldRingToSvgPath = (ring: LonLat[]): string =>
  ring
    .map(([lon, lat], index) => {
      const x = Number((((lon + 180) / 360) * 1000).toFixed(1));
      const y = Number((((90 - lat) / 180) * 500).toFixed(1));
      return `${index === 0 ? "M" : "L"}${x} ${y}`;
    })
    .join(" ")
    .concat(" Z");
